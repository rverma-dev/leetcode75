package neetcode

// [["5","3",".",".","7",".",".",".","."],["6",".",".","1","9","5",".",".","."],[".","9","8",".",".",".",".","6","."],["8",".",".",".","6",".",".",".","3"],["4",".",".","8",".","3",".",".","1"],["7",".",".",".","2",".",".",".","6"],[".","6",".",".",".",".","2","8","."],[".",".",".","4","1","9",".",".","5"],[".",".",".",".","8",".",".","7","9"]]
func IsValidSudoku(board [][]byte) bool {
	hr, vr, blocks := [9]byte{}, [9]byte{}, [9][9]byte{}

	for i, row := range board {
		for j, val := range row {
			di := blocks[3*(i%3)+j%3]
            val = val-'0'
			if hr[val] != 0 || vr[val] != 0 || di[val] != 0 {
				return false
			}
			hr[val]++
			vr[val]++
			di[val]++
		}
	}
	return true
}
